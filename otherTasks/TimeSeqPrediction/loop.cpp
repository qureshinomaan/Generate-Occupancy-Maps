#include<iostream>

using namespace std;

int main(void){
  
  cout<<"Starting i wala loop\n";
  for(int i=0;i<3;i++)
  {
    cout<<"hi i am i\n";
    cout<<"Starting j wala loop\n";
    for(int j=0;j<3;j++){
      cout<<"Hi I am j\n";
    }
    cout<<"End of j wala loop\n";
    
    
    cout<<"Starting k wala loop\n";
    for(int k=0;k<3;k++){
      cout<<"Hi I am k\n";
    }
    cout<<"End of k wala loop\n";
    
    
  }
  cout<<"End of i wala loop\n";
  return 0;
}
